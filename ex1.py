myset1 = set()   # create an empty set
# create a set from a list
myset2 = set(['a', 'b', 'c'])
print (myset2)

# create a set from a string
myset3 = set('xyz')
print (myset3)

# create a set the std way
myset4 = {'Alpha', 'Bravo', 'Charlie'}
print (myset4)

# take a string and convert it to a set
text = "Hello World!"
chars = set(text)
print(chars)